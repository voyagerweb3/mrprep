def seed():
    db = SessionLocal()
    try:
        existing = db.query(Category).count()
        if existing > 0:
            print(f"✅ База уже содержит {existing} категорий. Пропускаем seed.")
            return
        for cat_data in CATEGORIES:
            items_data = cat_data.pop("items")
            category = Category(**cat_data)
            db.add(category)
            db.flush()
            for i, item_data in enumerate(items_data):
                item = ChecklistItem(category_id=category.id, order=i, **item_data)
                db.add(item)
        db.commit()
        total_items = sum(len(c.get("items", [])) for c in CATEGORIES)
        print(f"✅ Добавлено {len(CATEGORIES)} категорий и {total_items} пунктов.")
    except Exception as e:
        db.rollback()
        print(f"❌ Ошибка: {e}")
        raise
    finally:
        db.close()

if __name__ == "__main__":
    seed()
